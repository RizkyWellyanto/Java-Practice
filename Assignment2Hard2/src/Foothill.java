   /* factorial method
    * pre-cond    : throw in a variable "n"
    * post-cond   : calculate log"n"! using a for loop and return the value
    */
   private static double logNFactorial(double number) 
   {
      double output = 0;
      for(double counter = 1; counter <= number; counter += 1) 
      {
         output += Math.log(counter);
      }
      return output;
   }
   
   /* calculates the log version of Stirling's formula
    * pre-cond    : throw in a variable "n"
    * post-cond   : calculate "n" using Stirling's formula and return the value
    */
   private static double logStirlingFormula(double number)
   {
      number = (number*Math.log(number))-number;
      return number;
   }
   
   /* calculates the error
    * pre-cond    : throw in the logNfactorial answer and logStirling's formula
    * post-cond   : calculate the difference and return it as %
    */
   private static double error(double logNFactorial, double stirlingFormula)
   {
      double output;
      output = (logNFactorial - stirlingFormula)/(double)logNFactorial;
      return output;
   }
   
   
   /* create a line
    * pre-cond    : throw in a "length" value and string "shape"
    * post-cond   : create a line with the length "length" and shape "shape"
    */
   private static void makeLine(int length, String shape)
   {
      for(int counter = 0; counter < length; counter += 1)
      {
         System.out.print(shape);
      }
   }
