import java.text.NumberFormat;
import java.util.Locale;

import cs_1c.FHsort;

public class Foothill
{
   public static void main(String[] args)
   {
      final int[] ARRAY_SIZE = {20000,40000,80000,100000,200000,400000,600000
            ,800000,1000000};
      
      for (int size : ARRAY_SIZE)
      {
         quickSortTest(size);
      }
   }
   
   public static void quickSortTest (int arraySize)
   {
      int recLimit, i, rdm;
      long startTime, stopTime;
      
      Integer[] randomArray = new Integer[arraySize];
      Integer[] targetArray = new Integer[arraySize];
      
      NumberFormat tidy = NumberFormat.getInstance(Locale.US);
      tidy.setMaximumFractionDigits(4);
      
      for (i = 0; i < arraySize; i++)
      {
         rdm = (int) (Math.random() * arraySize);
         randomArray[i] = rdm;
      }
      
      System.out.println("==============================================");
      System.out.println("||  Array Size       = "+ arraySize 
            +"              ||");
      System.out.println("==============================================");
      System.out.println("||  Recursive Limit  |     Time(second)     ||");
      System.out.println("==============================================");
      
      for (recLimit = 2; recLimit < 300; recLimit += 2)
      {
         for (i = 0; i < arraySize; i++)
         {
            targetArray[i] = randomArray[i];
         }
         
         FHsort.setRecursionLimit(recLimit);
         
         startTime = System.nanoTime();   // ------------------ start
         FHsort.quickSort(targetArray);
         stopTime = System.nanoTime();    // ---------------------- stop
         
         System.out.println("||         " + recLimit + "         |     "
               + tidy.format((stopTime - startTime) / 1e9) + "          ||");       
         
         // useful output format when importing txt file to excel file
         /*System.out.println(tidy.format( (stopTime - startTime) / 1e9) + "," + 
               arraySize + "," + recLimit); */
      }
      
      System.out.println("==============================================");
   }
}

